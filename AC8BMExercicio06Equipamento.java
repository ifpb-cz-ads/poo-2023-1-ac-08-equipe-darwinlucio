//Class Equipamento
public class AC8BMExercicio06Equipamento {
	
	private String nome;
	private double preco;
	
	public AC8BMExercicio06Equipamento (String nome, double preco) {
		this.nome = nome;
		this.preco = preco;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public double getPreco() {
		return preco;
	}
	
	public void setPreco(double preco) {
		this.preco = preco;
	}
}
